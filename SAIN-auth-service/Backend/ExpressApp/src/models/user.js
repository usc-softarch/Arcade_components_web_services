    const token = jwt.sign(
        { _id: user._id.toString() ,
            firstname:user.firstname,
            lastname:user.lastname,
            email:user.email,
            expire: parseInt(expireDate / 1000)
    }, process.env.Secret_key || config.Secret_key)

    user.tokens = { token }
    await user.save()

